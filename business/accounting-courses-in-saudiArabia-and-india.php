                                                    <h2 dir="ltr">Finance Course Projects</h2>
                                                    <div>
                                                        <p dir="ltr">During our finance courses, students will encounter specific
                                                            projects, such as the following:</p>
                                                        <p dir="ltr">
                                                            <strong>Financial Statement Analysis Project:</strong>
                                                        </p>
                                                        <p dir="ltr">Students will form study groups to analyze the financial statements
                                                            of a chosen publicly traded company. They will access SEC archives for the
                                                            financial statements, submit an executive summary of the analysis, and present
                                                            their findings. The analysis involves evaluating the company's financial health
                                                            by interpreting ratios, including those in the DuPont framework and other
                                                            relevant ratios.</p>
                                                        <p dir="ltr">Comparing ratios over time, benchmarking against industry peers,
                                                            and recognizing limitations of financial statement analysis are crucial.
                                                            Additionally, students must highlight key facts in each document - balance
                                                            sheet, income statement, cash flow statement, and notes.</p>
                                                        <p dir="ltr">
                                                            <strong>Small Business Valuation Project:</strong>
                                                        </p>
                                                        <p dir="ltr">Participants will work in groups to conduct a small business
                                                            valuation, acting as financial advisors to recommend a purchase price for an
                                                            investment fund. They will assume roles as managing partners of the investment
                                                            fund and select a small business for valuation. The valuation process must be
                                                            passive, prohibiting direct inquiries to business owners or employees for
                                                            financial data.&nbsp;</p>
                                                        <p dir="ltr">The project requires a written report and a presentation. Valuation
                                                            recommendations will be based on price multiples (market approach) and
                                                            discounted cash flow (DCF) analysis (income approach). Clarifying assumptions in
                                                            data generation, selecting comparable companies for multiples, and determining
                                                            the discount rate for DCF analysis are essential. Reconciling disparities
                                                            between multiples-based and DCF valuations is also required, with an example
                                                            provided involving Crumbl Cookies.</p>
